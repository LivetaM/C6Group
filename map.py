#The initilise function of this class works by loading each file into the map_files class and setting the
#image of each instance of the class based off of the map_files list
class Map(pygame.sprite.Sprite):
    def __init__(self, tile_map, y, x):
        pygame.sprite.Sprite.__init__(self)
        self.image = map_files[tile_map]
        self.rect = self.image.get_rect()

        self.x = x
        self.y = y

#Realign the map
    def update(self, cameraX, cameraY):
        self.rect.topleft = self.x - cameraX, self.y - cameraY

